import Foundation

enum Strings {
    enum App {
        static let name = String(localized: "app.name")
    }

    enum Tab {
        static let home = String(localized: "tab.home")
        static let clean = String(localized: "tab.clean")
        static let settings = String(localized: "tab.settings")
    }

    enum Home {
        static let storageTitle = String(localized: "home.storage.title")
    }

    enum Clean {
        static let start = String(localized: "clean.start")
    }

    enum Permission {
        static let photoTitle = String(localized: "permission.photo.title")
    }
}
